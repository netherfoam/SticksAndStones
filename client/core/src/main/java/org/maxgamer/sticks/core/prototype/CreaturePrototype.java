package org.maxgamer.sticks.core.prototype;

public class CreaturePrototype {
    private String animations;
    private String[] footstep;

    public String getAnimations() {
        return animations;
    }

    public void setAnimations(String animations) {
        this.animations = animations;
    }

    public String[] getFootstep() {
        return footstep;
    }

    public void setFootstep(String[] footstep) {
        this.footstep = footstep;
    }
}
